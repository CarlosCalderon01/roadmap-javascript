/**
        Los "Built-in objects" o "objetos incorporados" en JavaScript son objetos que
        están integrados en el propio lenguaje JavaScript y están disponibles globalmente
        en cualquier parte de tu código JavaScript. Estos objetos son parte de la especificación
        del lenguaje y proporcionan funcionalidades comunes que son ampliamente utilizadas
        en aplicaciones JavaScript.

    Aquí tienes algunos ejemplos de objetos incorporados en JavaScript:

        1. `Number`: Proporciona funcionalidades para trabajar con valores numéricos, como convertir cadenas en números, redondear números, entre otros.
        2. `Math`: Proporciona funciones matemáticas y constantes, como `Math.PI` o `Math.random()`.
        3. `Date`: Permite trabajar con fechas y horas, como crear objetos de fecha, obtener la fecha actual, etc.
        4. `String`: Proporciona funcionalidades para manipular cadenas de texto, como concatenar, dividir, buscar subcadenas, entre otras operaciones.
        5. `Error`: Se utiliza para representar errores en JavaScript y proporciona información sobre el tipo y la descripción del error.
        6. `Function`: Permite trabajar con funciones en JavaScript, como definir funciones, invocar funciones, y obtener información sobre las funciones.
        7. `Boolean`: Representa valores booleanos `true` y `false` y proporciona métodos para trabajar con ellos.

    Estos objetos incorporados son esenciales para desarrollar aplicaciones en JavaScript,
        ya que proporcionan una amplia gama de funcionalidades y utilidades.
        Puedes acceder a ellos en cualquier lugar de tu código JavaScript sin necesidad
        de importar librerías adicionales, ya que son parte integral del lenguaje.
        Esto facilita el desarrollo de aplicaciones web y
        otros tipos de aplicaciones utilizando JavaScript.

**/

